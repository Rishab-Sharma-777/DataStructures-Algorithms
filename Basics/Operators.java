public class Operators {
    public static void main(String args[]){
        int v1=10,v2=5;
        System.out.println("Add: "+(v1+v2));
        System.out.println("Sub: "+(v1-v2));
        System.out.println("Mul: "+(v1*v2));
        System.out.println("Mod: "+(v1/v2));
        System.out.println("Rem: "+(v1%v2));

    }
}
